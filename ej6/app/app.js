//FUNCIONES CALCULADORA
function sum (a,b,c){
    let suma = a + b + c
    return suma
}

function substrack(a, b,c){
    let rest = a-b-c
    return rest
}

function multiply(a,b,c){
    let multiply = a*b*c
    return multiply
}
function divide(a,b,c){
    let split = a/b/c
    return split
}

module.exports = {sum, substrack,multiply,divide}





