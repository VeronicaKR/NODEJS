//FUNCIONES CALCULADORA
class Calculator{

sum (a,b,c){
    let sum = a + b + c
    return sum
}


rest(a, b,c){
    let rest = a-b-c
    return rest
}

multiply(a,b,c){
    let multiply = a*b*c
    return multiply
}
split(a,b,c){
    let split = a/b/c
    return split
}
}

module.exports = Calculator;






